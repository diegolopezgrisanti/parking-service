package com.parkingapp.parkingservice.model;

public class Parking {
    private Integer id;
    private String name;

    public Parking() {
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
